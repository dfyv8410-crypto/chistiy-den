PHONE_REGEX = re.compile(
    r"(\+?7|8)[\s\-]?(\(?\d{3}\)?[\s\-]?\d{3}[\s\-]?\d{2}[\s\-]?\d{2})"
)
EMAIL_REGEX = re.compile(r"\b[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}\b")
ADDRESS_REGEX = re.compile(
    r"(?:г\.|город|ул\.|улица|пр\.|проспект|д\.|дом|кв\.|квартира|"
    r"корп\.|корпус|стр\.|строение|р-н|район|обл\.|область|"
    r"дер\.|деревня|пос\.|поселок|ш\.|шоссе|пл\.|площадь|"
    r"б-р|бульвар|пер\.|переулок)"
    r"\s+[а-яёА-ЯЁ\w\.\-]+",
    re.IGNORECASE,
)


def _contains_any(text: str, keywords: List[str]) -> bool:
    """Check if text contains any of the given keywords.

    Args:
        text: The text to search in.
        keywords: List of keyword strings to search for.

    Returns:
        True if at least one keyword is found in text.
    """
    lower_text = text.lower()
    for kw in keywords:
        if kw in lower_text:
            return True
    return False


class SafetyGuardrails:
    """Safety guardrails for validating AI responses and detecting crises.

    Provides methods to check responses for harmful content, detect crisis
    situations, validate tool suggestions, and sanitize user input.
    """

    def __init__(self) -> None:
        self._crisis_keywords: Dict[str, List[str]] = {
            "suicidal": CRISIS_KEYWORDS_SUICIDAL,
            "self_harm": CRISIS_KEYWORDS_SELF_HARM,
            "relapse": CRISIS_KEYWORDS_RELAPSE,
            "panic": CRISIS_KEYWORDS_PANIC,
            "isolation": CRISIS_KEYWORDS_ISOLATION,
        }

    def check_response(self, response: str, context: dict) -> dict:
        """Validate an AI response before sending it to the user.

        Scans the response for harmful content across multiple categories
        and returns a safety assessment with severity level.

        Args:
            response: The AI-generated response text.
            context: Additional context dict (e.g. user_id, conversation_id).

        Returns:
            A dict with keys:
                - safe (bool): Whether the response passed all checks.
                - issues (list[str]): What was flagged.
                - modified_response (str | None): Suggested replacement if unsafe.
                - severity (str): "none" | "low" | "medium" | "high".
        """
        issues: List[str] = []
        severity: str = "none"

        checks: List[Tuple[str, List[str], str]] = [
            ("medical_advice", MEDICAL_ADVICE_KEYWORDS, "medium"),
            ("dangerous", DANGEROUS_KEYWORDS, "high"),
            ("manipulative", MANIPULATIVE_KEYWORDS, "medium"),
            ("over_promising", OVER_PROMISING_KEYWORDS, "low"),
            ("ai_reference", AI_REFERENCE_KEYWORDS, "low"),
            ("religious_extremism", RELIGIOUS_EXTREMISM_KEYWORDS, "high"),
        ]

        for category_name, keywords, cat_severity in checks:
            if _contains_any(response, keywords):
                issues.append(category_name)
                severity = self._max_severity(severity, cat_severity)

        response_lower = response.lower()
        response_words = response_lower.split()
        for phrase in BANNED_PHRASES:
            pl = phrase.lower()
            if pl in response_lower:
                issues.append(f"banned_phrase: '{phrase}'")
                severity = self._max_severity(severity, "high")
                continue
            phrase_words = pl.split()
            if len(phrase_words) >= 2:
                idx = 0
                for w in response_words:
                    if w == phrase_words[idx]:
                        idx += 1
                        if idx == len(phrase_words):
                            issues.append(f"banned_phrase: '{phrase}'")
                            severity = self._max_severity(severity, "high")
                            break

        safe: bool = len(issues) == 0

        if not safe:
            modified_response = (
                "Я не могу ответить на это так, как планировал, "
                "потому что это может быть неуместно или вредно. "
                "Давай обсудим что-то другое или попробуем иначе."
            )
        else:
            modified_response = None

        return {
            "safe": safe,
            "issues": issues,
            "modified_response": modified_response,
            "severity": severity,
        }

    def check_crisis(self, text: str, context: dict) -> dict:
        """Enhanced crisis detection in user text.

        Analyses user input for signs of crisis across multiple types
        and determines severity level and appropriate response.

        Args:
            text: User input text.
            context: Additional context dict.

        Returns:
            A dict with keys:
                - crisis (bool): Whether a crisis is detected.
                - level (str): "none" | "mild" | "moderate" | "severe".
                - type (str): Crisis type identifier.
                - requires_immediate_action (bool).
                - suggested_response (str): What the AI should say.
        """
        if not text or not text.strip():
            return {
                "crisis": False,
                "level": "none",
                "type": "none",
                "requires_immediate_action": False,
                "suggested_response": CRISIS_RESPONSE_TEMPLATES["none"]["none"],
            }

        crisis_type: str = "none"
        crisis_level: str = "none"
        found_types: List[str] = []

        for ctype, keywords in self._crisis_keywords.items():
            if _contains_any(text, keywords):
                found_types.append(ctype)

        if not found_types:
            return {
                "crisis": False,
                "level": "none",
                "type": "none",
                "requires_immediate_action": False,
                "suggested_response": CRISIS_RESPONSE_TEMPLATES["none"]["none"],
            }

        word_count = len(text.split())
        text_len = len(text)

        severe_urgent_words = [
            "сейчас", "прямо сейчас", "немедленно", "уже", "умираю",
            "умирать", "смерть пришла", "прощай", "прощаться",
        ]
        moderate_markers = [
            "помоги", "не знаю что делать", "страшно", "плохо",
            "очень плохо", "тяжело", "не могу",
        ]
        mild_markers: List[str] = [
            "грустно", "одиноко", "устал", "трудно",
        ]

        has_urgent = _contains_any(text, severe_urgent_words)
        has_moderate = _contains_any(text, moderate_markers)
        has_mild = _contains_any(text, mild_markers)

        if has_urgent or word_count > 20 or text_len > 200:
            crisis_level = "severe"
        elif has_moderate or word_count > 10:
            crisis_level = "moderate"
        elif has_mild:
            crisis_level = "mild"
        else:
            crisis_level = "moderate"

        crisis_type = found_types[0]

        requires_immediate_action = crisis_level == "severe"

        response_key = crisis_level if crisis_level in CRISIS_RESPONSE_TEMPLATES else "moderate"
        type_key = crisis_type if crisis_type in CRISIS_RESPONSE_TEMPLATES[response_key] else "none"

        suggested_response = CRISIS_RESPONSE_TEMPLATES[response_key].get(
            type_key,
            CRISIS_RESPONSE_TEMPLATES[response_key]["none"],
        )

        return {
            "crisis": True,
            "level": crisis_level,
            "type": crisis_type,
            "requires_immediate_action": requires_immediate_action,
            "suggested_response": suggested_response,
        }

    def validate_tool_suggestion(self, tool: str) -> bool:
        """Check if a tool suggestion is allowed.

        Only tools from the predefined allowed list are accepted.

        Args:
            tool: Tool name to validate.

        Returns:
            True if the tool is in the allowed tools list.
        """
        return tool.strip().lower() in ALLOWED_TOOLS

    def sanitize_user_input(self, text: str) -> str:
        """Remove personal identifiable information from user input.

        Strips phone numbers, email addresses, and physical addresses
        before storing in memory or logs.

        Args:
            text: User input text.

        Returns:
            Sanitized text with PII replaced by placeholder markers.
        """
        sanitized = PHONE_REGEX.sub("[ТЕЛЕФОН_УДАЛЕН]", text)
        sanitized = EMAIL_REGEX.sub("[EMAIL_УДАЛЕН]", sanitized)
        sanitized = ADDRESS_REGEX.sub("[АДРЕС_УДАЛЕН]", sanitized)
        return sanitized

    @staticmethod
    def _max_severity(current: str, new: str) -> str:
        """Return the higher severity between two severity levels.

        Args:
            current: Current severity level.
            new: New severity level to compare.

        Returns:
            The higher severity string.
        """
        levels = ["none", "low", "medium", "high"]
        return levels[max(levels.index(current), levels.index(new))]
