def _compute_intensity(text: str, keywords_found: list) -> float:
    """Вычисляет интенсивность эмоции на основе текста.

    Факторы:
    - количество восклицательных знаков
    - процент заглавных букв (крик)
    - многоточия (надрыв)
    - количество найденных ключевых слов
    - длина сообщения
    - слова-усилители
    """
    base = 0.3
    length = len(text)

    # Восклицательные знаки
    exclamations = text.count("!")
    if exclamations >= 3:
        base += 0.3
    elif exclamations >= 1:
        base += 0.15

    # Заглавные буквы (крик)
    letters = [c for c in text if c.isalpha()]
    if letters:
        upper_ratio = sum(1 for c in letters if c.isupper()) / len(letters)
        if upper_ratio > 0.6:
            base += 0.25
        elif upper_ratio > 0.3:
            base += 0.1

    # Многоточия и повторы
    ellipsis = text.count("...") + text.count("...") + text.count("..")
    if ellipsis >= 2:
        base += 0.15
    elif ellipsis >= 1:
        base += 0.05

    # Повторяющиеся буквы (неееет, бееесит)
    repeats = len(re.findall(r"(.)\1{2,}", text))
    if repeats > 0:
        base += 0.1

    # Ключевые слова
    base += min(len(keywords_found) * 0.08, 0.3)

    # Длина сообщения (короткие эмоциональные сообщения — выше интенсивность)
    if length < 20 and base > 0.3:
        base += 0.1

    # Слова-усилители
    amplifiers = [
        "очень", "сильно", "невероятно", "чрезвычайно", "ужасно",
        "жутко", "безумно", "невыносимо", "дико", "реально", "серьёзно",
    ]
    words_lower = text.lower().split()
    for word in words_lower:
        if word.strip(",!.") in amplifiers:
            base += 0.05

    return min(max(base, 0.0), 1.0)


def _infer_cause(text: str, context: Optional[dict]) -> Optional[str]:
    """Пытается определить причину эмоционального состояния."""
    # Прямые указания в тексте
    cause_patterns = [
        (r"(?:из-за|потому что|так как|ведь|потому,? что) (.+?)[\.!?]?", "причина указана"),
        (r"меня (?:бесит|злит|раздражает|грустит|печалит) (.+?)[\.!?]?", "раздражитель"),
        (r"я (?:злюсь|грущу|печалюсь|бешусь) (?:на|из-за) (.+?)[\.!?]?", "объект эмоции"),
        (r"(?:случилось|произошло) (.+?)[\.!?]?", "событие"),
        (r"он[ао]? (?:меня|мне) (.+?)[\.!?]?", "взаимодействие"),
        (r"на работе|в школе|в универе|дома|в семье", "контекст"),
        (r"после (.+?)[\.!?]?", "временная связь"),
        (r"опять|снова|снова и снова|каждый раз", "повторяющаяся ситуация"),
    ]

    for pattern, _hint in cause_patterns:
        match = re.search(pattern, text.lower())
        if match:
            found = match.group(0).strip()
            if len(found) > 5:
                return found

    # Контекст из дневника
    if context:
        context_cause = context.get("cause") or context.get("trigger")
        if context_cause:
            return context_cause

    return None


def _match_emotion(text_lower: str):
    """Определяет первичную эмоцию и найденные ключевые слова."""
    emotion_keywords = {
        "joy": JOY_KEYWORDS,
        "sadness": SADNESS_KEYWORDS,
        "anxiety": ANXIETY_KEYWORDS,
        "anger": ANGER_KEYWORDS,
        "fear": FEAR_KEYWORDS,
        "shame": SHAME_KEYWORDS,
        "guilt": GUILT_KEYWORDS,
        "hope": HOPE_KEYWORDS,
        "gratitude": GRATITUDE_KEYWORDS,
        "loneliness": LONELINESS_KEYWORDS,
        "craving": CRAVING_KEYWORDS,
        "neutral": NEUTRAL_KEYWORDS,
    }

    best_emotion = "neutral"
    best_count = 0
    best_keywords = []
    matches = {}

    for emotion, keywords in emotion_keywords.items():
        found = [kw for kw in keywords if kw in text_lower]
        if found:
            matches[emotion] = found
            count = len(found)
            if count > best_count:
                best_count = count
                best_emotion = emotion
                best_keywords = found

    # Если найдено несколько эмоций с одинаковым количеством — mixed
    if best_count > 0:
        top_emotions = [e for e, kw in matches.items() if len(kw) == best_count]
        if len(top_emotions) >= 2:
            best_emotion = "mixed"
            best_keywords = []
            for e in top_emotions:
                best_keywords.extend(matches[e])

    return best_emotion, best_keywords


def _detect_crisis(text_lower: str) -> bool:
    """Определяет, является ли сообщение кризисным."""
    for kw in CRISIS_KEYWORDS:
        if kw in text_lower:
            return True
    return False


