"""
Emotion Analyzer — анализирует эмоциональное состояние пользователя по тексту.

Модуль предоставляет:
- EmotionState: dataclass с полным описанием эмоционального состояния
- EmotionAnalyzer: класс для анализа текста и выбора режима ответа
- analyze_sentiment_enhanced(): улучшенный анализ с keyword-matching
"""

import re
from typing import Optional


# ──────────────────────────────────────────────
#  Эмоциональное состояние
# ──────────────────────────────────────────────


class EmotionState:
    """Полное описание эмоционального состояния пользователя.

    Attributes:
        emotion:         Основная эмоция (joy, sadness, anxiety, anger, fear,
                         shame, guilt, hope, gratitude, loneliness, craving,
                         neutral, mixed).
        intensity:       Интенсивность от 0.0 до 1.0.
        cause:           Предполагаемая причина/триггер.
        need:            Что человеку нужно прямо сейчас.
        crisis:          Признак кризисного состояния.
    """

    __slots__ = ("emotion", "intensity", "cause", "need", "crisis")

    def __init__(
        self,
        emotion: str = "neutral",
        intensity: float = 0.0,
        cause: Optional[str] = None,
        need: str = "listening",
        crisis: bool = False,
    ):
        self.emotion = emotion
        self.intensity = intensity
        self.cause = cause
        self.need = need
        self.crisis = crisis

    def __repr__(self):
        return (
            f"EmotionState(emotion={self.emotion!r}, intensity={self.intensity}, "
            f"cause={self.cause!r}, need={self.need!r}, crisis={self.crisis})"
        )


