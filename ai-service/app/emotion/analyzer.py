class EmotionAnalyzer:
    """Анализирует эмоциональное состояние пользователя по тексту.

    Использует keyword-matching и контекст для определения эмоции,
    интенсивности, потребности и кризисного статуса.
    """

    def analyze(self, text: str, context: Optional[dict] = None) -> EmotionState:
        """Анализирует сообщение пользователя и возвращает эмоциональное состояние.

        Args:
            text:    Текст сообщения пользователя.
            context: Опциональный словарь с дополнительным контекстом:
                     - diary: недавние дневниковые записи
                     - cause: известная причина
                     - trigger: известный триггер
                     - user_id: идентификатор пользователя

        Returns:
            EmotionState с определённой эмоцией, интенсивностью, причиной,
            потребностью и признаком кризиса.
        """
        if not text or not text.strip():
            return EmotionState(
                emotion="neutral",
                intensity=0.0,
                cause=None,
                need="listening",
                crisis=False,
            )

        text_lower = text.lower().strip()
        context = context or {}

        # Определяем кризис
        crisis = _detect_crisis(text_lower)

        # Определяем эмоцию
        emotion, keywords_found = _match_emotion(text_lower)

        # Вычисляем интенсивность
        intensity = _compute_intensity(text, keywords_found)

        # Причина
        cause = _infer_cause(text, context)

        # Потребность
        need = NEED_BY_EMOTION.get(emotion, "listening")

        return EmotionState(
            emotion=emotion,
            intensity=round(intensity, 2),
            cause=cause,
            need=need,
            crisis=crisis,
        )

    def select_response_mode(self, emotion: EmotionState) -> str:
        """Выбирает режим ответа в зависимости от эмоционального состояния.

        Args:
            emotion: Объект EmotionState.

        Returns:
            Один из режимов: comfort, guidance, listening, action, hope,
            connection, validation, silence, celebration.
        """
        # Кризис — только direct/action режим
        if emotion.crisis:
            return "action"

        # Тихие, созерцательные состояния
        if emotion.intensity < 0.2 and emotion.emotion in ("neutral", "mixed"):
            return "silence"

        # Режим по эмоции
        return EMOTION_TO_MODE.get(emotion.emotion, "listening")


